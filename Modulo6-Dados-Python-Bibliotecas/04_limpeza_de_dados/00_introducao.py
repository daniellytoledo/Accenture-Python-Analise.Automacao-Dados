# Qualidade de dados

# Antes de analisar dados, precisamos garantir que eles possuem boa qualidade.
# Dados de baixa qualidade podem gerar:

# análises incorretas
# modelos imprecisos
# decisões erradas

# Por isso, uma etapa importante da análise de dados é a limpeza de dados (data cleaning)

