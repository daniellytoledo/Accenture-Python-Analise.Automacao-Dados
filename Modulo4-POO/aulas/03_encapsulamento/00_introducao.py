# O que é?

# O encasulamento é um dos conceitos fundamentais em programação orientada a objetos. Ele descreve a ideia de agrupar dados e os métodos que manipulam esses dados em uma unidade. Isso impoõe restrições ao acesso direto a variáveis e métodos e pode evitar a modificação acidental de dados. Para evitar alterações acidentais, a variável de um objeto só pode ser alterada pelo método desse objeto. Em outras palavras, estamos falando sobre a proteção de determinados dados para que ele não tenha acesso por pessoas que não deveriam ter permissão.

# Modificadores de Acesso

# Em linguagens como Java e C++, existem palavras reservadas para definir o nível de acesso aos atributos e métodos da classe. Em python não temos palavras reservadas, porém usamos convenções no nome do recurso, para definir se a variável é pública ou privada.

# Público: pode ser acessado de fora da classe.
# Privado: só pode ser acessado pela classe.

