# A palavra polimofirmo significa ter muitas formas. Na programação, polimorfismo significa o mesmo nome de função (mas assinauras diferentes) sendo usado para tipos diferente.

# O len() é um exemplo. ele consegue agir de várias maneiras dependendo de como você usa

print(len("Python")) # vai retornar quantas letras tem na palavra python
print(len([10, 20, 30])) # já retorna quantos elementos tem dentro da lista passada

